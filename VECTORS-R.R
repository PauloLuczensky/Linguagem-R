#Vectors

#THERE ARE 5 ATOMIC VECTORS, ALSO TERMED AS FIVE LASSES OF VECTORS
#1- LOGICAL
#2- INTEGER
#3- NUMERIC
#4- COMPLEX
#5- CHARACTER

#Logical
vtr1 = c(TRUE, FALSE)

#Numeric
vtr2 = c(15, 85.6747474, 99999)

#Integer
#Para considerar um inteiro, tem que colocar a letra L
vtr3 = c(35L, 58L, 34L)

#OBSERVAÇÕES
vtr4 = c(58.465L) # dará erro

vtr5 = c(TRUE, 35L, 3.14) #quando colocar vtr5 no console, 
#mostrará o valor 1 no True

vtr6 = c("Hello", FALSE, 65L)# quando colocar o vtr6 no console para print,
#todos serão transformados em caracteres
